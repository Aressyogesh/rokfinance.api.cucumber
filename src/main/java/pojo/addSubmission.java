package pojo;

public class addSubmission {
	
	private String external_id;
	private String opportunity_id;
	private String submission_id;
	private String submission_name;
	private String status;
	
	
	public void setexternal_id(String external_id) {
		this.external_id = external_id;
	}
	public String getexternal_id() {
		return external_id;
	}

	public void setopportunity_id(String opportunity_id) {
		this.opportunity_id = opportunity_id;
	}
	public String getopportunity_id() {
		return opportunity_id;
	}
	
	public void setsubmission_id(String submission_id) {
		this.submission_id = submission_id;
	}
	public String getsubmission_id() {
		return submission_id;
	}
	
	public void setsubmission_name(String submission_name) {
		this.submission_name = submission_name;
	}
	public String getsubmission_name() {
		return submission_name;
	}
	
	public void setstatus(String status) {
		this.status = status;
	}
	public String getstatus() {
		return status;
	}
	
		
}
